function y = idct_1d (x) ;
    % y = zeros (8, 1);
    X = x;
    % constants
    c1 = ga (1);
    c2 = ga (2);
    c3 = ga (3);
    c4 = ga (4);
    c5 = ga (5);
    c6 = ga (6);
    c7 = ga (7);

    % P
    y(1) =  X(1);
    y(2) =  X(5);
    y(3) =  X(3);
    y(4) =  X(7);
    y(5) = -X(2);
    y(6) = -X(4);
    y(7) =  X(8);
    y(8) = -X(6);

    % K
    %   g1
    X(1) = 0.5 * y(1) * c4;
    X(2) = 0.5 * y(2) * c4;

    %   g2
    a1 = 0.25 / c6 * y(3);
    a2 = 0.25 / c2 * y(4);
    a3 = c4 * (a1 + a2);
    X(3) = a1 - a2 - a3;
    X(4) = a3;

    %   g4
    %       d
    a1 = y(5) / c5;
    a2 = y(6) / c1;
    a3 = y(7) / c3;
    a4 = y(8) / c7;
    %       h42, 1 g1 g2, h41
    % a5 =  a1 - a3;
    % a6 = -a1 - a3;
    % a7 =  a2 - a4;
    % a8 =  a2 + a4;
    % a9 =  0.5 / c6 * a6;
    % a10 = 0.5 / c2 * a8;
    % a11 = c4 * (a9 + a10);
    % a12 = c4 * (a5 + a7);
    % a13 = a9 - a10 - a11;
    % X(5) = 0.5 * (a5 - a7 + a13);
    % X(6) = 0.5 * (a12 + a11);
    % X(7) = 0.5 * -a11;
    % X(8) = 0.5 * (a12 - a13);

    a5  =  a1 - a2 - a3 + a4;
    a6  =  a1 + a2 - a3 - a4;
    a7  = -a1      - a3;
    a8  =       a2      + a4;
    a9  = .5 / c6 * a7;
    a10 = .5 / c2 * a8;
    a11 = c4 * (a9 + a10);
    a12 = c4 * a6;
    a13 = a9 - a10 - a11;

    X(5) = 0.25 * (a5 + a13);
    X(6) = 0.25 * (a12 + a11);
    X(7) = 0.25 * -a11;
    X(8) = 0.25 * (a12 - a13);

    % B
    %   b1
    y(1) =  X(1) + X(2);
    y(2) =  X(1) - X(2);
    y(3) =  X(4);
    y(4) =  X(3);
    y(5) = -X(8);
    y(6) = -X(7);
    y(7) = -X(5);
    y(8) =  X(6);

    %   b2
    X(1) = y(1) + y(3);
    X(2) = y(2) + y(4);
    X(3) = y(2) - y(4);
    X(4) = y(1) - y(3);
    X(5) = y(5);
    X(6) = y(6);
    X(7) = y(7);
    X(8) = y(8);

    %   b3
    y(1) = X(1) + X(5);
    y(2) = X(2) + X(6);
    y(3) = X(3) + X(7);
    y(4) = X(4) + X(8);
    y(5) = X(4) - X(8);
    y(6) = X(3) - X(7);
    y(7) = X(2) - X(6);
    y(8) = X(1) - X(5);
